package Matematika;

public class mtk {
    void pertambahan(int a, int b){
        int result=a+b;
        System.out.println("Hasil dari "+a+"+"+b+" adalah "+result);
    }

    void pengurangan(int a, int b){
        int result=a-b;
        System.out.println("Hasil dari "+a+"-"+b+" adalah "+result);
    }

    void perkalian(int a, int b){
        int result=a*b;
        System.out.println("Hasil dari "+a+"*"+b+" adalah "+result);
    }

    void pembagian(int a, int b){
        int result=a/b;
        System.out.println("Hasil dari "+a+"/"+b+" adalah "+result);
    }
}
